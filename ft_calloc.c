#include "libft.h"

void	*ft_calloc(size_t nmemb, size_t size)
{
	size_t	total_size;
	void*	ptr;

	total_size = nmemb * size;
	ptr = (void *)malloc(total_size * sizeof(void));
	if (ptr != NULL)
		ft_bzero(ptr, total_size);
	return (ptr);
}
