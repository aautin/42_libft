#include "libft.h"

void	*ft_calloc(size_t nmemb, size_t size)
{
	void	*ptr;

	if ((nmemb * size < size + nmemb - 1) && size && nmemb)
		return (NULL);
	ptr = (void *)malloc((nmemb * size) * sizeof(void));
	if (ptr != NULL)
		ft_bzero(ptr, (nmemb * size));
	return (ptr);
}
