#include "../libft.h"

int		ft_is_in_str(char a, char *s)
{
	size_t	i;
	
	i = 0;
	while (s[i])
	{
		if (s[i] == a)
			return (1);
		i++;
	}
	return (0);
}

char 	*ft_strtrim(char const *s1, char const *set)
{
	size_t	i;
	size_t	start;
	size_t	end;
	char	*str;

	i = 0;
	start = 0;
	while (s1[i] && ft_is_in_str(s1[i], (char *) set))
		start = ++i;
	while (s1[i])
	{
		if (!ft_is_in_str(s1[i], (char *) set))
			end = i;
		i++;
	}
	str = (char *)malloc((end - start + 2) * sizeof(char));
	if (!str)
		return (NULL);
	i = 0;
	while (start <= end)
		str[i++] = s1[start++];
	str[i] = '\0';
	return (str); 
}
